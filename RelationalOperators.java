public class RelationalOperators {
    public static void main (String[] args){
        int x = 5;
        int y = 10;

        // Equal to 
        System.out.println("x==y :" + (x==y)); //false
        // Not Equal to 
        System.out.println("x!=y :" + (x!=y)); // true
        // greator than
        System.out.println("x>y:" + (x>y)); // false
        // less than 
        System.out.println("x<y:" + (x<y)); // true
        // Greater than or equal to
        System.out.println("x >= y: " + (x >= y));  // false 

        // Less than or equal to    
        System.out.println("x <= y: " + (x <= y));  // true
        
    }
}
